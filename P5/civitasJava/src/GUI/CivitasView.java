    Respuestas comprar() {
        int opcion = JOptionPane.showConfirmDialog(null, "¿Comprar la calle actual?", "Compra", JOptionPane.YES_NO_OPTION);
        Respuestas res;

        if (opcion == 0) 
            res = Respuestas.SI;
        else 
            res = Respuestas.NO;

        return res;
    }

