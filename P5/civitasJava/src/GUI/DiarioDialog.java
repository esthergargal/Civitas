    void mostrarEventos(){
        if(Diario.getInstance().eventosPendientes()){   // se muestra la ventana sÃ³lo si hay eventos pendientes 
            String evento = Diario.getInstance().leerEvento();
            
            while (evento != "") {
                eventos.setText(evento);
                System.out.println("\n --> " + evento + "\n");
                evento = Diario.getInstance().leerEvento();
            }
            
            eventos.setVisible(true);

            repaint();
            revalidate();
        }
    }
    
